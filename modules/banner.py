# ── Helpers ───────────────────────────────────────────────────────────────────

def _write(text: str) -> None:
    sys.stdout.write(text)
    sys.stdout.flush()


def _fade_lines(lines: list[str], delay: float = 0.025) -> None:
    for line in lines:
        print(f"{TEAL}{line}{RESET}")
        time.sleep(delay)


def _typewrite(text: str, delay: float = 0.022) -> None:
    for char in text:
        _write(char)
        time.sleep(delay)
    print()


def _pulse_bar(width: int = 54, delay: float = 0.010) -> None:
    _write(f"  {TEAL}[{RESET}")
    for _ in range(width):
        _write(f"{CYAN}━{RESET}")
        time.sleep(delay)
    _write(f"{TEAL}]{RESET}\n")


# ── Public API ────────────────────────────────────────────────────────────────

def print_banner() -> None:
    """
    Renders the animated Leviathan banner to stdout.
    Call once at the start of main() in core.py.
    """
    print()

    # 1 — Dragon body fades in line by line
    _fade_lines(_DRAGON_LINES, delay=0.018)

    # 2 — Wave + title
    print(_WAVE_LINE)
    time.sleep(0.04)
    print(_TITLE_LINE)
    time.sleep(0.04)
    print(_BASE_LINE)
    print()
    time.sleep(0.1)

    # 3 — Info block typewritten
    _typewrite(f"  {LBLUE}version  {WHITE}{BOLD}{VERSION}{RESET}", delay=0.018)
    _typewrite(f"  {LBLUE}author   {WHITE}{BOLD}{AUTHOR}{RESET}",  delay=0.018)
    _typewrite(f"  {LBLUE}about    {GREY}{DESC}{RESET}",           delay=0.014)
    print(f"  {GREY}{'─' * 54}{RESET}")
    print()
    time.sleep(0.15)

    # 4 — Loading bar
    _write(f"  {DIM}initializing ...{RESET}\n")
    time.sleep(0.1)
    _pulse_bar()
    print()
